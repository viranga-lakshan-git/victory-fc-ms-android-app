package com.example.laksh.victoryfc;

public class ListRunnersUp {
    int year;
    String description;

    public ListRunnersUp(int year, String description) {
        this.year = year;
        this.description = description;
    }

    public int getYear() {
        return year;
    }

    public String getDescription() {
        return description;
    }
}